public class Variablen2 {
	public static void main(String[] args) {
		
		//Initialisierung und Zuweisung
		char z1 = 'a';
		char z2 = 'b';
		char z3 = 'A';
		char z4 = 0x00a9; // Alternativ 169
		char z5 = 0x00be; // Darstellung in hex. Alternativ 190 dezimal

		//Ausgabe
		System.out.println("z1 = " + z1);
		System.out.println("z2 = " + z2);
		System.out.println("z3 = " + z3);
		System.out.println("z4 = " + z4);
		System.out.println("z5 = " + z5);
	}
}
